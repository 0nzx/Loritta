	fun whatIsTheNextDrop(quantity: Int): DropType? {
		return DropType.values().sortedBy {
			it.requiredPoints
		}.filter { it.requiredPoints > quantity }
				.firstOrNull()
	}

	fun alreadyReceivedDropTypes(quantity: Int): List<DropType> {
		return DropType.values().sortedBy {
