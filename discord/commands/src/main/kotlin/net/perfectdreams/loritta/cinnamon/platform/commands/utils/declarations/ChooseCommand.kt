import net.perfectdreams.loritta.cinnamon.platform.commands.CommandCategory
import net.perfectdreams.loritta.cinnamon.platform.commands.CinnamonSlashCommandDeclarationWrapper
import net.perfectdreams.loritta.cinnamon.platform.commands.utils.ChooseExecutor

class ChooseCommand(loritta: LorittaCinnamon) : CinnamonSlashCommandDeclarationWrapper(loritta) {
    companion object {
        val I18N_PREFIX = I18nKeysData.Commands.Command.Choose
    }

    override fun declaration() = slashCommand("choose", CommandCategory.UTILS, I18N_PREFIX.Description) {
        executor = ChooseExecutor(loritta)
    }
}